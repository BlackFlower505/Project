#include <iostream>

int main()
{
    double S, R;
    S = 3.14 * pow(R, 2);
    std::cout << "S = 3.14 * R**2" << std::endl;
    std::cout << "R: ";
    std::cin >> R;
    std::cout << std::endl;

    double S, a;
    S = pow(a, 2);
    std::cout << "S = a**2" << std::endl;
    std::cout << "a: ";
    std::cin >> a;
    std::cout << std::endl;

    double S, a, b;
    S = a * b;
    std::cout << "S = a**2" << std::endl;
    std::cout << "a: ";
    std::cin >> a;
    std::cout << std::endl;
    std::cout << "b: ";
    std::cin >> b;
    std::cout << std::endl;
}
